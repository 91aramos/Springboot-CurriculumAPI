CREATE TABLE SECTIONS
(
    ID       BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    COMPANY    VARCHAR(256) NOT NULL,
    DATES  VARCHAR(256) NOT NULL,
    DESCRIPTION    VARCHAR(500),
    RESPONSABILITIES    VARCHAR(256),
    TECHNOLOGIES    VARCHAR(256)
);

CREATE TABLE RESPONSABILITIES
(
    ID       BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ID_SECTIONS       BIGINT,
    TEXT    VARCHAR(500)
);

CREATE TABLE TECHNOLOGIES
(
    ID       BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ID_SECTIONS       BIGINT,
    TEXT    VARCHAR(500)
);

CREATE TABLE SKILLS
(
    ID       BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    DESCRIPTION    VARCHAR(500),
    SCORE    VARCHAR(256)
);
